package Primer1;

public class Operacije {

	
	private double x;
	private double  y;
	private double z;
	
	public Operacije(int x,int y,int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public void  setX(double x) {
		this.x = x;
	}
	
	public void  setY(double y) {
		this.y = y;
	}
	
	public void  setZ(double z) {
		this.z = z;
	}
}
